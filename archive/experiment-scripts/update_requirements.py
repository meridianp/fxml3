#!/usr/bin/env python
"""
Update requirements.txt with new dependencies for FXML4.

This script reads the current requirements.txt file, adds any missing dependencies,
and updates the file with the latest versions.
"""

import os
import re
import subprocess
import argparse

# Required packages for feature selection module
FEATURE_SELECTION_DEPS = [
    "scikit-learn>=1.2.0",
    "pandas>=1.5.0",
    "numpy>=1.20.0",
    "matplotlib>=3.5.0",
    "seaborn>=0.12.0",  # For better visualizations
    "shap>=0.40.0",     # Optional for SHAP values
    "mlxtend>=0.21.0",  # Optional for sequential feature selection
    "networkx>=2.7.0",  # For correlation clusters
]

# Optional packages for advanced feature selection
OPTIONAL_DEPS = [
    "boruta_py>=0.3.0",  # For Boruta feature selection
    "tqdm>=4.64.0",      # For progress bars in long-running operations
]


def read_current_requirements(file_path="requirements.txt"):
    """Read the current requirements.txt file."""
    if not os.path.exists(file_path):
        return []
    
    with open(file_path, 'r') as f:
        return [line.strip() for line in f if line.strip() and not line.startswith('#')]


def parse_requirement(req_str):
    """Parse a requirement string into name and version specifier."""
    # Extract package name (everything before any version specifier)
    match = re.match(r"([^\s<>=!~]+)", req_str)
    if not match:
        return None, None
    
    name = match.group(1).lower()
    
    # Extract version specifier (if any)
    version_match = re.search(r"([<>=!~].+)$", req_str)
    version = version_match.group(1) if version_match else None
    
    return name, version


def merge_requirements(current_reqs, new_reqs):
    """Merge current and new requirements, preferring new versions."""
    result = {}
    
    # First, add current requirements
    for req in current_reqs:
        name, version = parse_requirement(req)
        if name:
            result[name] = req
    
    # Then, add/update with new requirements
    for req in new_reqs:
        name, version = parse_requirement(req)
        if name and name not in result:
            result[name] = req
        elif name:
            # Check if we need to update
            current_name, current_version = parse_requirement(result[name])
            # Always use new requirement if specified
            result[name] = req
    
    return list(result.values())


def write_requirements(requirements, file_path="requirements.txt"):
    """Write requirements to file."""
    with open(file_path, 'w') as f:
        f.write("# Requirements for FXML4\n")
        f.write("# Generated by update_requirements.py\n\n")
        
        for req in sorted(requirements):
            f.write(f"{req}\n")


def check_installed_packages(requirements):
    """Check which packages are already installed."""
    installed = []
    not_installed = []
    
    for req in requirements:
        name, _ = parse_requirement(req)
        if not name:
            continue
            
        try:
            # Try to import the package
            __import__(name.replace('-', '_'))
            installed.append(name)
        except ImportError:
            not_installed.append(name)
    
    return installed, not_installed


def install_missing_packages(packages, upgrade=False):
    """Install missing packages."""
    if not packages:
        print("No packages to install.")
        return
    
    print(f"Installing {len(packages)} packages...")
    
    cmd = ['pip', 'install']
    if upgrade:
        cmd.append('--upgrade')
    
    cmd.extend(packages)
    
    try:
        subprocess.run(cmd, check=True)
        print("Installation completed successfully.")
    except subprocess.CalledProcessError:
        print("Error during installation. Please install manually.")


def main():
    """Main function."""
    parser = argparse.ArgumentParser(description="Update requirements.txt with new dependencies")
    parser.add_argument("--install", action="store_true", help="Install missing packages")
    parser.add_argument("--upgrade", action="store_true", help="Upgrade existing packages")
    parser.add_argument("--include-optional", action="store_true", help="Include optional dependencies")
    args = parser.parse_args()
    
    # Get current requirements
    current_reqs = read_current_requirements()
    print(f"Found {len(current_reqs)} existing requirements")
    
    # Prepare new requirements
    new_reqs = FEATURE_SELECTION_DEPS.copy()
    if args.include_optional:
        new_reqs.extend(OPTIONAL_DEPS)
    
    # Merge requirements
    merged_reqs = merge_requirements(current_reqs, new_reqs)
    print(f"Merged requirements list has {len(merged_reqs)} packages")
    
    # Write updated requirements
    write_requirements(merged_reqs)
    print(f"Updated requirements.txt with {len(merged_reqs)} packages")
    
    # Check installed packages
    if args.install or args.upgrade:
        installed, not_installed = check_installed_packages(merged_reqs)
        print(f"Found {len(installed)} installed packages and {len(not_installed)} missing packages")
        
        if args.install and not_installed:
            install_missing_packages(not_installed, upgrade=args.upgrade)
        elif args.upgrade and installed:
            install_missing_packages(installed, upgrade=True)


if __name__ == "__main__":
    main()